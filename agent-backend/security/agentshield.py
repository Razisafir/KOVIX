def quick_scan_file(file_path: str) -> SecurityReport:
    """One-shot scan a single file and return a report."""
    shield = AgentShield()
    start = time.time() * 1000
    shield.scan_file(file_path)
    duration = int(time.time() * 1000 - start)
    report = shield.generate_report()
    report.scanned_files = 1
    report.scan_duration_ms = duration
    return report


def quick_scan_directory(dir_path: str, extensions: Optional[List[str]] = None) -> SecurityReport:
    """One-shot scan a directory and return a report."""
    shield = AgentShield()
    return shield.scan_directory(dir_path, extensions)
