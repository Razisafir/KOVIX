class Severity(Enum):
    """Severity levels for security findings."""

    CRITICAL = "critical"
    HIGH = "high"
    MEDIUM = "medium"
    LOW = "low"
    INFO = "info"


class Grade(Enum):
    """Letter grade derived from the security score."""

    A = "A"  # 90-100
    B = "B"  # 80-89
    C = "C"  # 70-79
    D = "D"  # 60-69
    F = "F"  # <60


@dataclass
class SecurityFinding:
    """A single security issue found during scanning."""

    rule_id: str
    category: str
    severity: Severity
    message: str
    file_path: Optional[str] = None
    line_number: Optional[int] = None
    snippet: Optional[str] = None
    remediation: str = ""


@dataclass
class SecurityReport:
    """Aggregated results from a security scan."""

    grade: Grade = Grade.F
    score: int = 0
    findings: List[SecurityFinding] = field(default_factory=list)
    summary: Dict[str, int] = field(default_factory=dict)
    scanned_files: int = 0
    scan_duration_ms: int = 0
    scan_timestamp: str = field(default_factory=lambda: datetime.utcnow().isoformat())
    rules_triggered: Set[str] = field(default_factory=set)

    def to_markdown(self) -> str:
        """Generate a human-readable Markdown report."""
        lines: List[str] = [
            "# AgentShield Security Report",
            "",
            f"**Scan Date:** {self.scan_timestamp}",
            f"**Grade:** {self.grade.value} (Score: {self.score}/100)",
            f"**Files Scanned:** {self.scanned_files}",
            f"**Scan Duration:** {self.scan_duration_ms}ms",
            f"**Total Findings:** {len(self.findings)}",
            "",
            "## Severity Breakdown",
            "",
        ]
        for sev in ["critical", "high", "medium", "low", "info"]:
            count = self.summary.get(sev, 0)
            emoji = {"critical": "🔴", "high": "🟠", "medium": "🟡", "low": "🟢", "info": "🔵"}.get(
                sev, "⚪"
            )
            lines.append(f"- {emoji} **{sev.upper()}:** {count}")

        lines.extend(["", "## Findings", ""])

        if not self.findings:
            lines.append("✅ No security issues found!")
        else:
            for i, f in enumerate(self.findings, 1):
                severity_emoji = {
                    Severity.CRITICAL: "🔴",
                    Severity.HIGH: "🟠",
                    Severity.MEDIUM: "🟡",
                    Severity.LOW: "🟢",
                    Severity.INFO: "🔵",
                }.get(f.severity, "⚪")
                lines.extend(
                    [
                        f"### {i}. {f.rule_id} — {f.message}",
                        "",
                        f"- **Severity:** {severity_emoji} {f.severity.value.upper()}",
                        f"- **Category:** {f.category}",
                    ]
                )
                if f.file_path:
                    lines.append(f"- **File:** `{f.file_path}`")
                if f.line_number:
                    lines.append(f"- **Line:** {f.line_number}")
                if f.snippet:
                    lines.extend(["- **Snippet:**", f"  ```", f"  {f.snippet}", f"  ```"])
                lines.extend([f"- **Remediation:** {f.remediation}", ""])

        lines.extend(["---", "*Generated by AgentShield v1.0*"])
        return "\n".join(lines)

    def to_json(self) -> Dict[str, Any]:
        """Generate a JSON-serialisable dict for CI pipeline integration."""
        return {
            "grade": self.grade.value,
            "score": self.score,
            "scanned_files": self.scanned_files,
            "scan_duration_ms": self.scan_duration_ms,
            "scan_timestamp": self.scan_timestamp,
            "summary": self.summary,
            "rules_triggered": sorted(self.rules_triggered),
            "findings": [
                {
                    "rule_id": f.rule_id,
                    "category": f.category,
                    "severity": f.severity.value,
                    "message": f.message,
                    "file_path": f.file_path,
                    "line_number": f.line_number,
                    "snippet": f.snippet,
                    "remediation": f.remediation,
                }
                for f in self.findings
            ],
        }


