    def scan_file(self, file_path: str) -> List[SecurityFinding]:
        """Scan a single file for security issues.

        Args:
            file_path: Path to the file to scan.

        Returns:
            List of :class:`SecurityFinding` objects.
        """
        findings: List[SecurityFinding] = []
        path = Path(file_path)

        if not path.is_file():
            logger.warning("File not found: %s", file_path)
            return findings

        ext = path.suffix.lower()
        try:
            content = path.read_text(encoding="utf-8", errors="replace")
        except Exception as exc:
            logger.error("Cannot read %s: %s", file_path, exc)
            return findings

        lines = content.splitlines()

        for rule in self.RULES:
            if ext not in rule.applies_to:
                continue

            for line_idx, line in enumerate(lines, start=1):
                for match in re.finditer(rule.pattern, line):
                    finding = SecurityFinding(
                        rule_id=rule.id,
                        category=rule.category,
                        severity=rule.severity,
                        message=rule.description,
                        file_path=str(path.resolve()),
                        line_number=line_idx,
                        snippet=line.strip()[:200],
                        remediation=rule.remediation,
                    )
                    findings.append(finding)
                    self._rules_triggered.add(rule.id)
                    logger.debug(
                        "[%s] %s at %s:%d", rule.id, rule.description, file_path, line_idx
                    )

        self.findings.extend(findings)
        return findings

    def scan_directory(
        self, dir_path: str, extensions: Optional[List[str]] = None
    ) -> SecurityReport:
        """Scan an entire directory for security issues.

        Args:
            dir_path: Root directory to scan.
            extensions: If given, only scan files with these extensions.

        Returns:
            A :class:`SecurityReport` summarising all findings.
        """
        start_time = time.time() * 1000
        root = Path(dir_path)

        if not root.is_dir():
            raise ValueError(f"Not a directory: {dir_path}")

        target_exts = set(extensions) if extensions else None
        files_scanned = 0
        all_findings: List[SecurityFinding] = []

        for file_path in root.rglob("*"):
            if not file_path.is_file():
                continue
            if file_path.name.startswith("."):
                continue  # skip hidden files

            ext = file_path.suffix.lower()
            if target_exts and ext not in target_exts:
                continue

            # Skip common non-source directories
            parts = set(file_path.parts)
            if parts & {"node_modules", ".git", "__pycache__", ".venv", "venv", "dist", "build"}:
                continue

            findings = self.scan_file(str(file_path))
            all_findings.extend(findings)
            files_scanned += 1

        duration_ms = int(time.time() * 1000 - start_time)
        return self._build_report(all_findings, files_scanned, duration_ms)

    def scan_config(self, config: Dict[str, Any]) -> List[SecurityFinding]:
        """Scan an agent or application configuration dictionary.

        Args:
            config: Configuration dict (e.g., from a JSON/YAML file).

        Returns:
            List of :class:`SecurityFinding` objects.
        """
        findings: List[SecurityFinding] = []
        config_str = json.dumps(config, indent=2, default=str)
        lines = config_str.splitlines()

        for rule in self.RULES:
            for line_idx, line in enumerate(lines, start=1):
                for match in re.finditer(rule.pattern, line):
                    finding = SecurityFinding(
                        rule_id=rule.id,
                        category=rule.category,
                        severity=rule.severity,
                        message=rule.description,
                        file_path="<config>",
                        line_number=line_idx,
                        snippet=line.strip()[:200],
                        remediation=rule.remediation,
                    )
                    findings.append(finding)
                    self._rules_triggered.add(rule.id)

        self.findings.extend(findings)
        return findings

    def scan_mcp_config(self, mcp_config: Dict[str, Any]) -> List[SecurityFinding]:
        """Scan an MCP server configuration for risks.

        Args:
            mcp_config: MCP configuration dict.

        Returns:
            List of :class:`SecurityFinding` objects.
        """
        findings: List[SecurityFinding] = []

        servers = mcp_config.get("servers", mcp_config.get("mcpServers", {}))
        if isinstance(servers, dict):
            for name, server_conf in servers.items():
                # Check for HTTP URLs
                url = server_conf.get("url", "")
                if url.startswith("http://") and not any(
                    h in url for h in ("localhost", "127.0.0.1", "0.0.0.0")
                ):
                    findings.append(
                        SecurityFinding(
                            rule_id="MCP-001",
                            category="mcp_risk",
                            severity=Severity.HIGH,
                            message=f"MCP server '{name}' uses unencrypted HTTP",
                            file_path="<mcp_config>",
                            snippet=f"url: {url}",
                            remediation="Use HTTPS for all MCP server connections",
                        )
                    )

                # Check for wildcard scopes
                scopes = server_conf.get("scopes", [])
                if "*" in scopes:
                    findings.append(
                        SecurityFinding(
                            rule_id="MCP-002",
                            category="mcp_risk",
                            severity=Severity.HIGH,
                            message=f"MCP server '{name}' has wildcard scopes",
                            file_path="<mcp_config>",
                            snippet=f"scopes: {scopes}",
                            remediation="Use minimal required scopes",
                        )
                    )

                # Check for disabled SSL verification
                if server_conf.get("verify_ssl") is False:
                    findings.append(
                        SecurityFinding(
                            rule_id="MCP-003",
                            category="mcp_risk",
                            severity=Severity.MEDIUM,
                            message=f"MCP server '{name}' has SSL verification disabled",
                            file_path="<mcp_config>",
                            snippet="verify_ssl: false",
                            remediation="Always verify SSL certificates in production",
                        )
                    )

                # Check for auth token in config
                if server_conf.get("auth_token") and not server_conf.get("auth_token", "").startswith(
                    "${"
                ):
                    findings.append(
                        SecurityFinding(
                            rule_id="MCP-004",
                            category="secrets",
                            severity=Severity.CRITICAL,
                            message=f"Hardcoded auth token for MCP server '{name}'",
                            file_path="<mcp_config>",
                            snippet=f"auth_token: {'*' * 10}",
                            remediation="Use environment variable references for auth tokens",
                        )
                    )

        self.findings.extend(findings)
        return findings

    # ------------------------------------------------------------------ #
    # Auto-fix
    # ------------------------------------------------------------------ #

    def auto_fix(self, report: SecurityReport) -> Dict[str, str]:
        """Apply safe automatic fixes for findings where possible.

        Currently supports:
        - Replacing ``yaml.load()`` → ``yaml.safe_load()``
        - Replacing ``eval()`` → ``ast.literal_eval()``
        - Replacing ``os.system()`` → ``subprocess.run()``
        - Setting ``DEBUG = False``

        Args:
            report: The security report to fix.

        Returns:
            Mapping of finding index → fix description.
        """
        fixes: Dict[str, str] = {}

        for i, finding in enumerate(report.findings):
            key = str(i)
            if finding.rule_id == "SEC-037":
                fixes[key] = "Replace yaml.load() with yaml.safe_load()"
            elif finding.rule_id == "SEC-030":
                fixes[key] = "Replace eval() with ast.literal_eval() or json.loads()"
            elif finding.rule_id == "SEC-034":
                fixes[key] = "Replace os.system() with subprocess.run(..., shell=False)"
            elif finding.rule_id == "SEC-044":
                fixes[key] = "Set DEBUG = False in production"
            elif finding.rule_id == "SEC-046":
                fixes[key] = "Set debug=False in Flask app.run()"
            elif finding.rule_id == "SEC-042":
                fixes[key] = "Replace random with secrets module for crypto operations"
            elif finding.rule_id == "SEC-040" or finding.rule_id == "SEC-041":
                fixes[key] = "Replace MD5/SHA1 with hashlib.sha256()"
            elif finding.rule_id == "SEC-032" or finding.rule_id == "SEC-033":
                fixes[key] = "Set shell=False and pass command as a list"
            elif finding.rule_id == "SEC-024":
                fixes[key] = "Set trust_env=False or configure proxies explicitly"
            elif finding.rule_id == "SEC-023":
                fixes[key] = "Remove verify_ssl=False; use proper certificates"

        return fixes

    # ------------------------------------------------------------------ #
    # Report generation
    # ------------------------------------------------------------------ #

    def generate_report(self) -> SecurityReport:
        """Generate a report from all findings collected so far."""
        return self._build_report(self.findings, 0, 0)

    def _build_report(
        self, findings: List[SecurityFinding], scanned_files: int, duration_ms: int
    ) -> SecurityReport:
        """Compute grade and summary from findings."""
        summary: Dict[str, int] = {
            "critical": 0,
            "high": 0,
            "medium": 0,
            "low": 0,
            "info": 0,
        }

        score = 100
        for f in findings:
            sev_key = f.severity.value
            summary[sev_key] = summary.get(sev_key, 0) + 1
            deduction = self.DEDUCTIONS.get(f.severity, 0)
            score -= deduction

        score = max(0, score)
        grade = self._score_to_grade(score)

        return SecurityReport(
            grade=grade,
            score=score,
            findings=findings,
            summary=summary,
            scanned_files=scanned_files,
            scan_duration_ms=duration_ms,
            rules_triggered=self._rules_triggered,
        )

    @staticmethod
    def _score_to_grade(score: int) -> Grade:
        if score >= 90:
            return Grade.A
        elif score >= 80:
            return Grade.B
        elif score >= 70:
            return Grade.C
        elif score >= 60:
            return Grade.D
        return Grade.F

    def clear_findings(self) -> None:
        """Clear all collected findings."""
        self.findings.clear()
        self._rules_triggered.clear()

    def add_custom_rule(self, rule: SecurityRule) -> None:
        """Add a user-defined security rule.

        Args:
            rule: The :class:`SecurityRule` to add.
        """
        self.RULES.append(rule)
        logger.info("Added custom security rule: %s (%s)", rule.id, rule.description)

    # ------------------------------------------------------------------ #
    # Dunder helpers
    # ------------------------------------------------------------------ #

    def __repr__(self) -> str:
        return f"<AgentShield rules={len(self.RULES)} findings={len(self.findings)}>"


