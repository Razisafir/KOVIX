"""
AgentShield — Security scanning inspired by ECC's AgentShield.

Categories (30+ key rules):
- Secrets detection: API keys, tokens, passwords
- Permission auditing: excessive permissions
- MCP server risk: untrusted servers, excessive scopes
- Agent config review: dangerous tool access
- Hook injection: malicious patterns
- Dependency vulnerabilities: known CVEs

Output: A-F grade, JSON report, Markdown report
"""

